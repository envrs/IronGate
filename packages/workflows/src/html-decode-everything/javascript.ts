function decodeXMLEntities(str: string) {
  return str.replace(
    new RegExp(Object.keys(entities).join("|"), "g"),
    function (match) {
      return entities[match as keyof typeof entities];
    },
  );
}

function decodeNumericEntities(str: string) {
  str = str.replace(/&#([0-9]+);/gi, function (match, numStr) {
    const num = parseInt(numStr, 10);
    return String.fromCharCode(num);
  });
  str = str.replace(/&#x([0-9a-f]+);/gi, function (match, numStr) {
    const num = parseInt(numStr, 16);
    return String.fromCharCode(num);
  });
  return str;
}

export function run(input: BytesInput, sdk: SDK) {
  const parsed = sdk.asString(input);
  return decodeXMLEntities(decodeNumericEntities(parsed));
}
